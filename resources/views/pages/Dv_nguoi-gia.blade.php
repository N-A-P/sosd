<p style="text-align: justify;">Công ty TNHH Dịch Vụ HTP tự hào là một trong  những công ty đi đầu về chất lượng dịch vụ <em><strong>giúp việc gia đình</strong></em> . Lấy tiêu chí “Vừa lòng khách đến, hài lòng khách đi” công ty có đội ngũ nhân viên được đào tạo nghiệp vụ chuyên nghiệp, luôn được nâng cao kỹ năng qua từng ngày từng tháng. Có đạo đức, nhân phẩm tốt, làm việc gắn bó lâu dài. Đến với <span style="color: #ff6600;"><em>dịch vụ giúp</em> <em>việc gia đình</em></span> của <strong>“HTP”</strong> các bạn sẽ cảm nhận được sự “Tận tâm – Trách nhiệm” của mỗi nhân viên tư vấn của gia đình bạn trong suốt quy trình làm việc.</p>
