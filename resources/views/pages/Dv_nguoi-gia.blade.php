<p style="text-align: justify;">Cuộc sống quá nhiều bận rộn, dường như các ông bố, bà mẹ đã quên đi mất sự tồn tại của “ngôi nhà”, nơi “bão dừng sau cánh cửa”. Ngày này qua ngày khác, nhà chỉ là nơi đi đi về về, không có hơi ấm. Mình vô cái nhà, dù biệt thự đẹp đến mấy, thấy bếp lạnh lẽo, bạn có ổn không? Dù bạn tài giỏi, xinh đẹp đến mấy mà ngôi nhà không có lửa, thì cũng chỉ như một ngôi nhà hoang vắng. Bạn có thấy, những ngôi nhà tranh, những ngôi nhà gỗ nhỏ nhưng vẫn ấm cũng vì có một bếp lửa hồng.</p>
